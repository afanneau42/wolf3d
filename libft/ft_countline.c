#include "libft.h"

int		ft_countline(char *str)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if (str[i] == '\n')
			j++;
		i++;
	}
	if (str[i - 1] != '\n')
		j++;
	return (j);
}

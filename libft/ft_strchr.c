#include "libft.h"

char		*ft_strchr(const char *s, int c)
{
	char		c1;
	int			i;

	i = 0;
	c1 = c;
	while (s[i] != c1 && s[i])
		i++;
	if (s[i] == c1)
		return ((char *)s + i);
	return (0);
}
